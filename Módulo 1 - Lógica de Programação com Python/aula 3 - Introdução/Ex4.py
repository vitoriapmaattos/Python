"""Crie um programa que receba o nome, idade, peso, altura e
telefone do usuário e mostre na tela as informações no seguinte formato:
"""

nome = input("Digite seu nome: ")
idade = input("Digite sua idade: ")
peso = input("Digite seu peso: ")
