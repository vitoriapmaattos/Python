"""
Faça um programa que receba 3 números, calcule o quadrado
de cada um deles e mostre o resultado na tela.
"""

valor_1 = int(input("Digite o primeiro valor: "))
valor_2 = int(input("Digite o segundo valor: "))
valor_3 = int(input("Digite o terceiro valor: "))

print(f"O {valor_1} ao quadrado é: {(valor_1 ** 2)}")
print(f"O {valor_2} ao quadrado é: {(valor_2 ** 2)}")
print(f"O {valor_3} ao quadrado é: {(valor_3 ** 2)}")